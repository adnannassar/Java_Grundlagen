package Advanced.HashSet;

import java.util.HashSet;

public class Hashset {
    public static void main(String[] args) {
        HashSet<Integer> hashSet = new HashSet<>();
        hashSet.add(10);
        hashSet.add(2);
        hashSet.add(4);
        hashSet.add(30);
        System.out.println(hashSet);
    }
}

